'use strict'

// SWITCH

var age = 18
var print1 = ""

switch (age) {

    case 18:
        print1 = "Already of legal age"
        break;
    case 25:
        print1 = "Already an adult"
        break;

    case 40:
        print1 = "Not 30 anymore"
        break;

    case 75:
        print1 = "Elder"
        break;

}

console.log(print1)







